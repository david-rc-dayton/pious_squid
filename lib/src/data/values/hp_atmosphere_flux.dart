/// Harris-Priester atmospheric density tables accounting for f10.7 solar flux.
class HpAtmosphereFluxData {
  /// Create a new [HpAtmosphereFluxData] object.
  HpAtmosphereFluxData()
      : _hMin = 110,
        _hMax = 2000,
        _fMin = 65,
        _fMax = 275;

  final double _hMin;
  final double _hMax;
  final double _fMin;
  final double _fMax;

  /// Return atmospheric parameters for a given [height] above Earth's
  /// surface _(km)_ and F10.7 solar [flux] _(1e-22 W/m²/Hz)_.
  HpAtmosphereResult? getAtmosphere(final double height, final double flux) {
    if (height < _hMin || height > _hMax) {
      return null;
    }
    final cflux = flux.clamp(_fMin, _fMax);
    var f0 = 65.0;
    var f1 = 75.0;
    var d0 = _hp65Data;
    var d1 = _hp75Data;
    if (cflux >= 75 && cflux < 100) {
      f0 = 75.0;
      f1 = 100.0;
      d0 = _hp75Data;
      d1 = _hp100Data;
    } else if (cflux >= 100 && cflux < 125) {
      f0 = 100.0;
      f1 = 125.0;
      d0 = _hp100Data;
      d1 = _hp125Data;
    } else if (cflux >= 125 && cflux < 150) {
      f0 = 125.0;
      f1 = 150.0;
      d0 = _hp125Data;
      d1 = _hp150Data;
    } else if (cflux >= 150 && cflux < 175) {
      f0 = 150.0;
      f1 = 175.0;
      d0 = _hp150Data;
      d1 = _hp175Data;
    } else if (cflux >= 175 && cflux < 200) {
      f0 = 175.0;
      f1 = 200.0;
      d0 = _hp175Data;
      d1 = _hp200Data;
    } else if (cflux >= 200 && cflux < 225) {
      f0 = 200.0;
      f1 = 225.0;
      d0 = _hp200Data;
      d1 = _hp225Data;
    } else if (cflux >= 225 && cflux < 250) {
      f0 = 225.0;
      f1 = 250.0;
      d0 = _hp225Data;
      d1 = _hp250Data;
    } else if (cflux >= 250 && cflux < 275) {
      f0 = 250.0;
      f1 = 275.0;
      d0 = _hp250Data;
      d1 = _hp275Data;
    }
    final r0 = d0.getAtmosphere(height);
    final r1 = d1.getAtmosphere(height);
    if (r0 == null || r1 == null) {
      return null;
    }
    final r00 = r0.hp0;
    final r01 = r0.hp1;
    final r10 = r1.hp0;
    final r11 = r1.hp1;
    final minD0 = linearInterpolate(cflux, f0, r00.minD, f1, r10.minD);
    final minD1 = linearInterpolate(cflux, f0, r01.minD, f1, r11.minD);
    final maxD0 = linearInterpolate(cflux, f0, r00.maxD, f1, r10.maxD);
    final maxD1 = linearInterpolate(cflux, f0, r01.maxD, f1, r11.maxD);
    return HpAtmosphereResult(
      height,
      (h: r00.h, minD: minD0, maxD: maxD0),
      (h: r11.h, minD: minD1, maxD: maxD1),
    );
  }
}

